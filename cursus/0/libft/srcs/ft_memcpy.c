#include "../libft.h"

void	*ft_memcpy(void *dest, const void *src, size_t n)
{
	char	*v1;
	char	*v2;

	if (!dest && !src)
		return (NULL);
	v1 = (char *)dest;
	v2 = (char *)src;
	while (n-- > 0)
		*(v1++) = *(v2++);
	return (dest);
}
