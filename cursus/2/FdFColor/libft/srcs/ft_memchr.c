#include "libft.h"

void	*ft_memchr(const void *str, int c, size_t n)
{
	char	*ret;

	if (!str)
		return (NULL);
	ret = (char *)str;
	while (n-- > 0)
	{
		if (*ret == (char)c)
			return ((void *)ret);
		ret++;
	}
	return (NULL);
}
