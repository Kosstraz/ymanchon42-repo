#include "../libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void *))
{
	t_list	*tmp;

	tmp = *lst;
	while (tmp)
	{
		del(tmp->item);
		free(tmp);
		tmp = (*lst)->next;
	}
	lst = (void *)0;
}